package com.java24hours;

class PlanetWeight {
    public static void main(String[] arguments) {
        System.out.print("Your weight on earth is ");
        double weight = 178;
        System.out.println(weight);

        System.print("Your weight on Mervury is ");
        double mercury = weight * 0.378;
        System.out.println(mercury);

        System.out.print("Your weight on the Moon is ");
        double moon = weight * 0.166;
        System.out.println(moon);

        System.out.print("Your weight on Jupitor is ");
        double jupitor = weight * 2.364;
        System.out.println(jupitor);
    }
}